num1 = int(input("Enter the first number  "))
num2 = int(input("Enter the second number  "))

###ADDITIONAL  OPERATOR ###
add = num1 + num2
print("Additional value = ",add)

### SUBSTRACTION OPERATOR ###
sub = num1-num2
print("Substraction value =",sub)

### MULTIPLICATION OPERATOR ###
mul = num1*num2
print("Multiplication value =",mul)

### DIVISION OPERATOR ###
div = num1/num2
print("Division value = ",div)

### MODULUS OPERATOR ###
mod = num1%num2
print("Modulus value",mod)

### INTEGER DIVISION or FLOOR VALUE OPERATOR ###
flr = num1//num2
print("Floor value",flr)

### EXPONENTIAL VALUES ###
exp = num1**num2
print("Exponential value",exp)
