num1 = int(input("Enter the first number"))
num2 = int(input("Enter the second number"))

### Greater than relation ###
print(num1>num2)

### LESS THAN RELATION ###
print(num1<num2)

### GREATER THAN OR EQUAL TO ###
print(num1>=num2)

### LESS THAN OR EQUAL TO ###
print(num1<=num2)

### EQUAL TO OPERATOR ###
print(num1==num2)

### NOT EQUAL TO OPERATOR ###
print(num1!=num2)